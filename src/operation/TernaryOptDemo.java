package operation;

public class TernaryOptDemo {
    public static void main(String[] args) {
        int a = 10;
        // Ternary operator
        int b = (a < 10) ? 20 : 30;
        System.out.println(b);

        /**
         * short of if-else
         * int b;
         * if (a < 10) {
         *     b = 20;
         * } else {
         *     b = 30;
         * }
         */

    }
}
